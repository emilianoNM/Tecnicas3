#Ubicacion de la Empresa

class Ubicacion(object):
	"""docstring for Ubicacion"""
	def __init__(self, Pais, Estado, Municipio, Localidad, Calle, Numero, CodigoPostal, altitud, latitud):
		
		self.Pais = Pais
		self.Estado = Estado
		self.Municipio = Municipio
		self.Localidad = Calle
		self.Calle = Calle
		self.Numero = Numero
		self.CodigoPostal = CodigoPostal
		self.altitud = altitud
		self.latitud = latitud
